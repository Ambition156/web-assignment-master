// 获取图片列表容器
const imgList = document.querySelector('.imgList')

const imgNavList = document.querySelector('.classifyList')
// 获取壁纸分类导航
const imgNav = document.querySelectorAll('.classifyItem')

// 初始化渲染
function render(template, val) {
  var html = val.reduce((preve, cur) => {
    return (
      preve +
      ` <li class="imgItem">
          <img src="${cur.smallPicurl}" alt="">
          <div class="imgDesc">${cur.description}</div>
        </li>`
    )
  }, ' ')

  // 渲染小说列表
  template.innerHTML = html
}

// 渲染壁纸
render(imgList, meinv)

// 点击切换分类 通过事件委托来实现
imgNavList.addEventListener('click', (e) => {
  const target = e.target
  //判断点击的是标签 而不是空白 toLowerCase() 将大写字母转成小写
  if (target.nodeType === 1 && target.nodeName.toLowerCase() === 'li') {
    // 移除所有active样式
    Array.prototype.forEach.call(imgNav, (item) =>
      item.classList.remove('active')
    )
    // 添加当前点击的样式
    target.classList.add('active')

    // 切换对应的分类
    switch (target.innerText) {
      case '明星美女':
        render(imgList, meinv)
        break
      case '帅哥靓仔':
        render(imgList, hdbody)
        break
      case '可爱萌宠':
        render(imgList, pet)
        break
      case 'AI美图':
        render(imgList, AI)
        break
      case '平铺卡通':
        render(imgList, cartoon)
        break
      case '自然风光':
        render(imgList, nature)
        break
      case '创意色彩':
        render(imgList, createColor)
        break
      default:
        render(imgList, meinv)
        break
    }
    window.onload()
  }
})

// 等待DOM加载完成后执行
window.onload =  () => {
  const novelItems = imgList.querySelectorAll('.imgItem')
  Array.prototype.forEach.call(novelItems, (item) => {
    item.onclick = (e) => {
      location.href = '../pages/imgDetil.html'
    }
  })
}
